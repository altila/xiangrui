class PublicAction extends ABaseAction {

	/**
	+----------------------------------------------------------
	* 清楚缓存
	+----------------------------------------------------------
	* @access  public
	+----------------------------------------------------------
	* @return  str
	+----------------------------------------------------------
	*/
	public function clearCache() {
		delFile( C('DATA_CACHE_PATH') );
		//清楚成功
		$this->assign( "jumpUrl", getReturnUrl() );
		$this->ajaxReturn( '', "清楚成功!", 1 );
	}

}
