class SiteRoleUserAction extends ABaseAction {

	/**
	+----------------------------------------------------------
	* 新增前置
	+----------------------------------------------------------
	*/
	public function _before_add() {
		if( empty($_REQUEST['sid']) || empty($_REQUEST['rid']) ) $this->ajaxReturn( '', "请选择站点或角色!", 0 );
	}

	/**
	+----------------------------------------------------------
	* 排序前置
	+----------------------------------------------------------
	*/
	public function _before_sort() {
		if( empty($_REQUEST['sid']) || empty($_REQUEST['rid']) ) $this->ajaxReturn( '', "请选择站点或角色!", 0 );
	}

	/**
	+----------------------------------------------------------
	* 默认插入前需要检验的字段
	+----------------------------------------------------------
	*/
	public function _filterInsertCheck() {
		$map['sid'] = $_REQUEST['sid'];
		$map['rid'] = $_REQUEST['rid'];
		$map['uid'] = $_POST['uid'];
		return $map;
	}

	/**
	+----------------------------------------------------------
	* 插入前置
	+----------------------------------------------------------
	*/
	public function _before_insert() {
		$_POST['uid'] = $_REQUEST['dwz_orgId_uid'];
	}


}
